function elemKey(es: ZodiacElement, em: ZodiacElement): string {
  return `${es}|${em}`
}

function pickVariant<T>(arr: readonly T[], sun: ZodiacSlug, moon: ZodiacSlug): T {
  const si = idx(sun)
  const mi = idx(moon)
  return arr[(si * 12 + mi) % arr.length]!
}

function pickRisingLine(rising: ZodiacSlug, sun: ZodiacSlug, moon: ZodiacSlug): string {
  const arr = RISING_MASK[rising]
  const ri = idx(rising)
  const si = idx(sun)
  const mi = idx(moon)
  return arr[(si + mi + ri) % arr.length]!
}

/**
 * 基于示意三轴落点的点睛句（确定性）。不含真实宫位/相位计算。
 */
export function triadInsightBlurb(sun: ZodiacSlug, moon: ZodiacSlug, rising: ZodiacSlug): string {
  if (sun === moon) {
    return `${SAME_SIGN[sun]} ${pickRisingLine(rising, sun, moon)}`
  }

  const pairKey = `${sun}|${moon}` as const
  const paired = PAIR_BODY[pairKey]
  if (paired) {
    return `${paired} ${pickRisingLine(rising, sun, moon)}`
  }

  const es = elementFromSign(sun)
  const em = elementFromSign(moon)
  const key = elemKey(es, em)
  const variants = ELEMENT_SYNTH[key]
  const body = pickVariant(variants, sun, moon)
  return `${body} ${pickRisingLine(rising, sun, moon)}`
}
